package Designpatterns;

public class Iterator {
	    private String name;
	     
	    public Iterator(String name) {
	        super();
	        this.name = name;
	    }
	 
	    public String getName() {
	        return name;
	    }
	 
	    public void setName(String name) {
	        this.name = name;
	    }
	}

